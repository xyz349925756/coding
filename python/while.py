numbers = input("请输入几个数字，用逗号分隔：").split(",")
#使用input()捕捉输入，按照提示输入几个数，并用逗号分隔。input()根据输入的逗号，生成一个列表。
print(numbers)#输出列表numbers的内容
x = 0  #定义变量x为0
while x < len(numbers):   #代码循环列表numbers
    print(numbers[x])     #输出列表中第x+1个数的值
    x += 1                #每次循环使变量x增1
 
