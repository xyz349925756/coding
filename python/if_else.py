a = input("a:")  #定义变量a
a = int(a)       #把用户输入的a转换为int类型
b = input("b:")
b = int(b)
if a > b:       #判断a,b的大小
    print(a," > ", b) #假设a=2 b=1 那么a>b成立。输出结果a>b
elif a < b:
    print(a," < ",b)
else:
    print(a," = ",b)
